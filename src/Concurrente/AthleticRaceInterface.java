    private void AddRunner(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddRunner
        if(counter <=4 ){
            System.out.println( String.valueOf( counter));
            Runners[counter]=new Runner(jtf_NombreCorredor.getText());
            Runners[counter].theRunner(buffer,jta_Resultados);
            System.out.println("Nuevo corredor: " + Runners[counter].getName() + "Velocidad: "+ Runners[counter].getSpeed() );
            
            JOptionPane.showMessageDialog(null,
                "Nombre: " + Runners[counter].getName()+  "\n Velocidad: "+ Runners[counter].getSpeed()  ,
                "Corredor registrado",
                JOptionPane.INFORMATION_MESSAGE);
            jtf_NombreCorredor.setText("");
            jta_CorredoresRegistrados.append(String.valueOf( counter+1)+ " - " + Runners[counter].getName()+"\n"   );

            counter ++ ;
        }
    }//GEN-LAST:event_AddRunner

    private void iniciarCarrera(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarCarrera
        if(counter==5){
            JOptionPane.showMessageDialog(null
                ,"Iniciando carrera" 
                ,"Carrera"
                ,JOptionPane.INFORMATION_MESSAGE);
            Runners[0].runForestRun();
            Runners[1].runForestRun();
            Runners[2].runForestRun();
            Runners[3].runForestRun();
            Runners[4].runForestRun();
            
        }else JOptionPane.showMessageDialog(null
                ,"No se han registrado los cinco corredores necesarios para iniciar la carrera." 
                ,"Esperando corredores"
                ,JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_iniciarCarrera

    private void reiniciarCarrera(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reiniciarCarrera
        counter = 0;
        for (int i = 0; i < 5; i++) {
            Runners[i].setName("");
            jta_CorredoresRegistrados.setText("");
            jta_Resultados.setText("");
        }

    }//GEN-LAST:event_reiniciarCarrera

